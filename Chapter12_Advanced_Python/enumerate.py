l1 = [3, 56, 877, 67]

for index, item in enumerate(l1):
    print(f"The number at index {index} is {item}.")
# No need to declare another variable and increment it in a loop
# With 'enumerate' we can traverse the value of 'index'
# both 'index' &  'item' can me renamed as we want, its just a variable name